def recursiveFunction(base, expo):

    if expo == 0:
        return 1
    else:
        base *= base
        recursiveFunction(base, expo-1)

    return base

def main():
    base = int(input("Digite o valor do expoente: "))
    expo = int(input("Digite o valor da base: "))

    print(f"O valor da multiplicação é: {recursiveFunction(base, expo)}")

main()