package a05_Array_Classes;

public class a01_array {
    public static void main(String[] args) {

        //Forma de declarar vetores, também com um tipo fixo ao contrário do Python
        int[] primos = {1,2,3,5,7,11,13};

        //foreach
        for (int primo: primos)
            System.out.println(primo);
    }
}
